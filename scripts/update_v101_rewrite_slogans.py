def main():
    conn = sqlite3.connect(str(DB))
    conn.row_factory = sqlite3.Row

    # 1) P34/P35/P36/P39 일괄 교체
    updates = [
        (33, "유튜브 콘텐츠 (인플루언서 협업 등) 기획 및 제작", P34_CONTENT),
        (34, "대학 광고 디자인 및 광고 제작 (인쇄)", P35_CONTENT),
        (35, "디지털 광고 (유튜브 · 인스타 · 페이스북) 콘텐츠 제작", P36_CONTENT),
        (38, "SNS 이벤트 및 콘텐츠 활성화", P39_CONTENT),
    ]
    for idx, title, content in updates:
        conn.execute(
            "UPDATE sections SET title=?, content=? WHERE proposal_id=? AND order_idx=?",
            (title, content, PID, idx),
        )
        print(f"P{idx+1} (idx={idx}) 재작성: 3슬로건 중심 / 표 형태")

    # 2) 예산 슬라이드 찾아서 "연 125억" → "연 1.25억" 수정 + 쉼표 정리
    budget_rows = conn.execute(
        "SELECT id, order_idx, content FROM sections WHERE proposal_id=? AND content IS NOT NULL ORDER BY order_idx",
        (PID,),
    ).fetchall()

    for r in budget_rows:
        c = r["content"]
        orig = c

        # 125억 → 1.25억 (또는 12.5억, 또는 1.25억) — 사용자 명시는 1.25억
        c = re.sub(r"125억", "1.25억", c)
        c = re.sub(r"연 125억", "연 1.25억", c)
        c = re.sub(r"125,?000,?000", "125,000,000", c)  # 125000000 → 1.25억 단위 재확인 필요
        # 쉼표 단위 정리: 억/만 숫자 표기 유지. 단, 큰 숫자 rule
        # 예: 1250000 → 1,250,000
        def add_commas(m):
            num = m.group(1)
            if len(num) >= 4:
                # 3자리씩 쉼표
                reversed_num = num[::-1]
                chunks = [reversed_num[i:i+3] for i in range(0, len(reversed_num), 3)]
                return ",".join(chunks)[::-1]
            return num
        # 숫자 뒤에 '원'이 오는 경우만 (금액)
        c = re.sub(r"(\d{4,})원", lambda m: add_commas(re.match(r"(\d+)", m.group(1))) + "원", c)

        if c != orig:
            conn.execute("UPDATE sections SET content=? WHERE id=?", (c, r["id"]))
            print(f"  P{r['order_idx']+1} 금액 표기 조정")

    conn.commit()
    conn.close()
    print("\n완료")


if __name__ == "__main__":
    main()
